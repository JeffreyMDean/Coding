// Write a while loop that prints the numbers 0 through 100, increasing by 5 each time.

function numberPrinter5() {
  let number = 0;

  while (number <= 100) {
    console.log(number);
    number += 5;
  }
}

numberPrinter5();

// Write a while loop that prints the number 9000 ten times.

function numberPrinter() {
  let count = 0;
  while (count < 10) {
    console.log(9000);
    count++;
  }
}

numberPrinter();
